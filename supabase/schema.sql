-- REACTIONS (like/upvote/etc)
do $$
begin
  if not exists (select 1 from pg_type where typname = 'reaction_target') then
    create type public.reaction_target as enum ('post', 'comment');
  end if;

  if not exists (select 1 from pg_type where typname = 'reaction_type') then
    create type public.reaction_type as enum ('like');
  end if;
end
$$;

create table if not exists public.reactions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles (id) on delete cascade,
  target public.reaction_target not null,
  post_id uuid references public.posts (id) on delete cascade,
  comment_id uuid references public.comments (id) on delete cascade,
  type public.reaction_type not null default 'like',
  created_at timestamptz not null default now(),
  constraint reactions_target_fk_check check (
    (target = 'post' and post_id is not null and comment_id is null) or
    (target = 'comment' and comment_id is not null and post_id is null)
  )
);

create index if not exists reactions_user_id_idx on public.reactions (user_id);
create index if not exists reactions_post_id_idx on public.reactions (post_id);
create index if not exists reactions_comment_id_idx on public.reactions (comment_id);

create unique index if not exists reactions_unique_user_post_like
on public.reactions (user_id, post_id, type)
where target = 'post';

create unique index if not exists reactions_unique_user_comment_like
on public.reactions (user_id, comment_id, type)
where target = 'comment';

