-- POSTS
create table if not exists public.posts (
  id uuid primary key default gen_random_uuid(),
  author_id uuid not null references public.profiles (id) on delete cascade,
  category_id bigint not null references public.categories (id) on delete restrict,
  title text not null,
  body text not null,
  is_anonymous boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- Length constraints (defense in depth)
do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'posts_title_len'
      and conrelid = 'public.posts'::regclass
  ) then
    alter table public.posts
      add constraint posts_title_len check (char_length(title) <= 200);
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conname = 'posts_body_len'
      and conrelid = 'public.posts'::regclass
  ) then
    alter table public.posts
      add constraint posts_body_len check (char_length(body) <= 15000) not valid;
  end if;
end
$$;

